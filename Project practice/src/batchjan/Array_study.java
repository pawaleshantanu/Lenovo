package batchjan;

public class Array_study {

	public static void main(String[] args) {
		String s []=new String [3];
		s[0]="Shantanu";
		s[1]="S";
		s[2]="Pawale";
		System.out.println(s[0]);
		System.out.println(s[1]);
		System.out.println(s[2]);	
		for(int i=0;i<s.length;i++) {
			System.out.println(s[i]);
		}

	}

}
